    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if(txtNamaBarang.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Nama Barang Kosong !");
            txtNamaBarang.requestFocus();
        }
        else if(txtStok.getText().equals("0")){
            JOptionPane.showMessageDialog(null, "Stok Barang Kosong !");
            txtStok.requestFocus();
        }
        else{
            java.sql.Connection conn= new Database().getConnection();            
        try{
            java.sql.PreparedStatement stmt=conn.prepareStatement("insert into stok (Id,nama,kategori,satuan,qty) values (?,?,?,?,?)");
            try{           
                stmt.setString(1, txtId.getText()); 
                stmt.setString(2,txtNamaBarang.getText());
                stmt.setString(3,cbKategori.getSelectedItem().toString());
                stmt.setString(4,cbSatuan.getSelectedItem().toString());
                stmt.setString(5,txtStok.getText());               
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(null,"MENYIMPAN DATA","Pesan",JOptionPane.INFORMATION_MESSAGE);
                Refresh();
               
            } catch(SQLException ex){
                JOptionPane.showMessageDialog(null,"GAGAL MENYIMPAN DATA !!!","Pesan",JOptionPane.WARNING_MESSAGE);
            }
        }catch(Exception e){

        }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

